public class datatypes {

    public static void main (String [] args)
    {
        //1. Integer
        int a = 10;
        a=20;
        int b =30;
        int c = 40;
        int d = -50;
        int e = 0;

        //2. Double
        double f = 20.33;
        double g = 23.3456;
        double h = 100;

        //3. Character - Accept only single value and should be written with in single quotes

        char i = 'A';
        char j = 'a';
        char k = '1';

        //4. Boolean - Accept only True or False

        boolean l = true;
        boolean m = false;

        //5. String - Not a data type but it is a class and should be written in double quotes
        String n = "Hi";
        String o = "Selenium practice";
        String p = "100";
        String q = "23.33";

        System.out.println(a);
        System.out.println(f);
        System.out.println(i);
        System.out.println(l);
        System.out.println(o);
        System.out.println(c+d);



    }

}
