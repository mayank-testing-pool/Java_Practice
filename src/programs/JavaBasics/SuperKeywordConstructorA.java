package src.programs.JavaBasics;

public class SuperKeywordConstructorA {


    public SuperKeywordConstructorA()
    {
        System.out.println( "Parent Class Constructor" );
    }

    public SuperKeywordConstructorA(int i)
    {
        System.out.println( "Parent Class Constructor and Value of i = "+i );
    }

    public SuperKeywordConstructorA(int i,int j)
    {
        System.out.println( "Parent Class Constructor and Value of i = "+i+ " and j = "+j);
    }



}


